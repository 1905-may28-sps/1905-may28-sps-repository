package com.revature.pojos;

public class Account {

	private int id;
	private double balance;
	private int accountNumber;
	private int userId;
	private int typeId;
	private String username;
	
	public Account() {}

	public Account(int id, double balance, int accountNumber, int userId, int typeId) {
		super();
		this.id = id;
		this.balance = balance;
		this.accountNumber = accountNumber;
		this.userId = userId;
		this.typeId = typeId;
	}

	public Account(int id, double balance, int accountNumber, int userId, int typeId, String username) {
		super();
		this.id = id;
		this.balance = balance;
		this.accountNumber = accountNumber;
		this.userId = userId;
		this.typeId = typeId;
		this.username = username;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getBalance() {
		return balance;
	}

	public void setBalance(double balance) {
		this.balance = balance;
	}

	public int getAccountNumber() {
		return accountNumber;
	}

	public void setAccountNumber(int accountNumber) {
		this.accountNumber = accountNumber;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getTypeId() {
		return typeId;
	}

	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}

	

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	@Override
	public String toString() {
		return "Account [id=" + id + ", balance=" + balance + ", accountNumber=" + accountNumber + ", userId=" + userId
				+ ", typeId=" + typeId + ", username=" + username + "]";
	}
}
